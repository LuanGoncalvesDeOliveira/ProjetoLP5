    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        
       if(incluir==true){
           //INSERT
            Util.mensagem("Cadastro salvo com sucesso!");
       }else{
           //UPDATE
            Util.mensagem("Modificado com sucesso!");
         }
        Util.habilitar(true,jBtnIncluir,jBtnPesquisar,jBtnExcluir,jBtnAlterar);
        Util.habilitar(false,jBtnConfirmar,jBtnCancelar,jTxtNome,jTxtCodigo,jTxtApelido,jPwfSenha,jCboNivel,jChbAtivo);
        Util.limpar(jTxtNome,jTxtCodigo,jTxtApelido,jPwfSenha,jCboNivel,jChbAtivo);

    
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

